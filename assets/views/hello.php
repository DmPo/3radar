                <div class="faq">
                    <h3>FAQ</h3>
                    <strong>— А це законно?</strong>
                    <p>— Так.</p>
                    <strong>— Якими законами це регулюється?</strong>
                    <p>
                        — Законами України
                        <a href='http://zakon2.rada.gov.ua/laws/show/595-19'>«Про місцеві вибори»</a>
                        ,
                        <a href='http://zakon2.rada.gov.ua/laws/show/93-15'>«Про статус депутатів місцевих рад»</a>
                        та
                        <a href='http://zakon2.rada.gov.ua/laws/show/280/97-%D0%B2%D1%80'>«Про місцеве самоврядування в Україні»</a>
                        .
                    </p>
                    <strong>— Так же можна всіх обранців відкликати?</strong>
                    <p>— Так. Крім депутатів Верховної ради.</p>
                    <strong>— І доведеться обирати нових?</strong>
                    <p>— Так.</p>
                    <strong>— Чи не використає хтось цей інструмент проти хороших обранців?</strong>
                    <p>— Це може статись навіть з чудовим обранцем. Як і завершення строку повноважень.</p>
                    <strong>— І у владу потраплять погані обранці?</strong>
                    <p>— Так. І їх можна буде відкликати також.</p>
                    <strong>— А якщо не вийде?</strong>
                    <p>— То можна спробувати ще.</p>
                    <strong>— На яку партію чи політика ви працюєте?</strong>
                    <p>— На жодну. Навпаки - ми примушуємо їх працювати на громадян.</p>
                    <strong>— Хто за вами стоїть?</strong>
                    <p>— Ніхто, крім Вас й таких, як Ви. За нами наш досвід протидії фальсифікаціям виборів.</p>
                    <strong>— Хто фінансує ваш проект?</strong>
                    <p>— Такі громадяни, як Ви. Інколи — донори, яких ми не приховуємо.</p>
                    <strong>— Хіба це не безкоштовно?</strong>
                    <p>— Консультації та література безкоштовно. Тренінги потребують більшого часу, тому необхідна компенсація.</p>
                    <strong>— Так це бізнес?!</strong>
                    <p>— Ні. Але інколи допомога громадянам потребує фінансування. Ми не приховуємо джерел свого фінансування.</p>
                    <strong>— Ви ж ніби проти заробляння на владі?!</strong>
                    <p>— Так. Ми не чиновники й не заробляємо на владі та використанні державних повноважень. Ви не платите нам податки. Коли Ви купуєте пресу, Ви також фінансуєте прозорість, доступ до інформації та контроль за владою (звісно, якщо Ви купуєте справді незалежну та якісну пресу). Ми поширюємо інформацію про Ваші права та методи їх захисту переважно безкоштовно.</p>
                    <strong>— Ви порвали мій шаблон!</strong>
                    <p>— Не дуже боляче? Чудово! Ми любимо допомагати людям й робити це без насилля. :)</p>
                    <strong>— Ви все це серйозно?</strong>
                    <p>— Абсолютно!</p>
